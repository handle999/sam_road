# 0. CityScale 验证
# ============================================================
print("=== CityScale ===")
CS_DIR = '/home/hanhaoyu/workspace/research/sam_road/datasets/cityscale/20cities'
CS_PROCESSED = '/home/hanhaoyu/workspace/research/sam_road/datasets/cityscale/processed'
CS_IMAGE_SIZE = 2048

for sample_id in ['0', '10']:
    rgb_path = os.path.join(CS_DIR, f'region_{sample_id}_sat.png')
    gt_png_path = os.path.join(CS_DIR, f'region_{sample_id}_gt.png')
    gt_graph_path = os.path.join(CS_DIR, f'region_{sample_id}_refine_gt_graph.p')
    
    if not os.path.exists(rgb_path) or not os.path.exists(gt_graph_path):
        print(f"[SKIP] region_{sample_id} - files not found")
        continue
    
    rgb = read_rgb(rgb_path)
    gt_png = cv2.imread(gt_png_path, cv2.IMREAD_GRAYSCALE) if os.path.exists(gt_png_path) else None
    existing_road = cv2.imread(os.path.join(CS_PROCESSED, f'road_mask_{sample_id}.png'), cv2.IMREAD_GRAYSCALE)
    
    with open(gt_graph_path, 'rb') as f:
        gt = pickle.load(f)
    
    cs_transforms = {
        'swap (cityscale)': lambda v: v[:, ::-1],
        'spacenet (flip-y)': lambda v: np.stack([v[:, 1], CS_IMAGE_SIZE - v[:, 0]], axis=1),
        'no-transform (raw)': lambda v: v,
    }
    
    fig, axes = plt.subplots(1, 5, figsize=(25, 5))
    axes[0].imshow(rgb)
    if gt_png is not None:
        axes[0].imshow(gt_png, cmap='jet', alpha=0.3)
    axes[0].set_title("GT.png overlay\n(reference)")
    
    axes[1].imshow(rgb)
    if existing_road is not None:
        axes[1].imshow(existing_road, cmap='jet', alpha=0.4)
    axes[1].set_title("Existing road_mask\n(generated by script)")
    
    for i, (name, tf) in enumerate(cs_transforms.items()):
        road_mask, _ = draw_graph_mask(gt, CS_IMAGE_SIZE, tf)
        ax = axes[i+2]
        ax.imshow(rgb)
        ax.imshow(road_mask, cmap='jet', alpha=0.4)
        ax.set_title(f"pickle + {name}\noverlay on RGB")
    
    plt.suptitle(f"CityScale region_{sample_id}: Which coord_transform matches GT.png?", fontsize=13)
    plt.tight_layout()
    plt.savefig(os.path.join(OUTPUT_DIR, f'cityscale_region_{sample_id}_quick_check.png'), dpi=150)
    plt.close()
    print(f"Saved: cityscale_region_{sample_id}_quick_check.png")


