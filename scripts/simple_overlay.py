"""
SAM-Road 简洁版坐标系验证脚本
================================
用 GT.png 作为参考，在 RGB 上叠加不同坐标变换生成的 road mask，
快速验证哪种 coord_transform 是正确的。

使用方法:
  cd /home/hanhaoyu/workspace/research/sam_road
  conda activate samroad
  python scripts/simple_overlay.py

输出文件索引 (全部保存在 outputs/viz/ 目录下):
================================================

  cityscale_region_{N}_quick_check.png
    含义: CityScale region_N, GT.png + processed mask vs 3种变换
    布局: 1×5 子图
      - 第1列: RGB + GT.png 叠加 (正确参考)
      - 第2列: RGB + 已有的 processed/road_mask_{N}.png 叠加 (generate_labels.py生成)
      - 第3列: RGB + swap(cityscale) 变换的 road_mask
      - 第4列: RGB + spacenet(flip-y) 变换的 road_mask
      - 第5列: RGB + raw(无变换) 的 road_mask
    用途: 对比GT.png、已有mask、和3种变换, 验证cityscale坐标系
    验证结论: swap 与GT.png高度一致, flip-y不一致

  spacenet_quick_check.png
    含义: SpaceNet 第1个样本, GT.png vs 3种变换生成的 road mask
    布局: 1×4 子图
      - 第1列: RGB + GT.png 叠加 (正确参考, GT.png是数据集自带的道路mask)
      - 第2列: RGB + swap(cityscale) 变换的 road_mask
      - 第3列: RGB + spacenet(flip-y) 变换的 road_mask
      - 第4列: RGB + raw(无变换) 的 road_mask
    用途: 一眼对比哪种变换的road_mask与GT.png更一致
    验证结论: spacenet(flip-y) 与GT.png高度一致, swap不一致(IoU仅0.03)

  xian_region_{N}_quick_check.png
    含义: Xian region_N, GT.png + 已有processed mask vs 3种变换
    布局: 1×5 子图
      - 第1列: RGB + GT.png 叠加 (正确参考)
      - 第2列: RGB + 已有的 processed/road_mask_{N}.png 叠加 (generate_labels.py生成)
      - 第3列: RGB + swap(cityscale) 变换的 road_mask
      - 第4列: RGB + spacenet(flip-y) 变换的 road_mask
      - 第5列: RGB + raw(无变换) 的 road_mask
    用途: 对比GT.png、已有mask、和3种变换, 验证xian坐标系
    验证结论: swap 与GT.png高度一致(IoU=0.61), flip-y不一致(IoU=0.10)

控制台输出:
  还会打印 SpaceNet/Xian pickle 的 dim0 分布统计, 以及关键推断:
  - CityScale pickle 使用左上原点 (图像坐标系), 只需 swap
  - SpaceNet pickle 使用左下原点 (数学坐标系), 需要 flip-y
  - Xian pickle 使用左上原点 (图像坐标系), 只需 swap
"""
import os, pickle
import numpy as np
import cv2
import matplotlib
matplotlib.use('Agg')
import matplotlib.pyplot as plt

OUTPUT_DIR = '/home/hanhaoyu/workspace/research/sam_road/outputs/viz'
os.makedirs(OUTPUT_DIR, exist_ok=True)

def read_rgb(path):
    bgr = cv2.imread(path)
    return cv2.cvtColor(bgr, cv2.COLOR_BGR2RGB)

def draw_graph_mask(gt_graph, image_size, coord_transform):
    """生成 road mask 和 keypoint mask"""
    import networkx as nx
    graph = nx.Graph()
    for n, neis in gt_graph.items():
        for nei in neis:
            n_arr = np.array([[n[0], n[1]]], dtype=np.float64)
            nei_arr = np.array([[nei[0], nei[1]]], dtype=np.float64)
            tn = coord_transform(n_arr)[0]
            tnei = coord_transform(nei_arr)[0]
            graph.add_edge((int(tn[0]), int(tn[1])), (int(tnei[0]), int(tnei[1])))
    
    road_mask = np.zeros((image_size, image_size), dtype=np.uint8)
    for (x1, y1), (x2, y2) in graph.edges():
        cv2.line(road_mask, (x1, y1), (x2, y2), 255, 3)
    
    key_nodes = [node for node, degree in graph.degree() if degree != 2]
    keypoint_mask = np.zeros((image_size, image_size), dtype=np.uint8)
    for point in key_nodes:
        cv2.circle(keypoint_mask, point, 3, 255, -1)
    
    return road_mask, keypoint_mask


